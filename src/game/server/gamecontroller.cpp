bool IGameController::PreSpawn(CPlayer* pPlayer, vec2 *pOutPos)
{
	int Team = pPlayer->GetTeam();
	
	// spectators can't spawn
	if(Team == TEAM_SPECTATORS)
		return false;

	int Type = (pPlayer->IsInfect() ? TEAM_RED : TEAM_BLUE);

	// get spawn point
	for(int i = 0; i < m_aaSpawnPoints[Type].size(); i++)
	{
		if(IsSpawnable(m_aaSpawnPoints[Type][i]))
		{
			*pOutPos = m_aaSpawnPoints[Type][i];
			return true;
		}
	}
	
	return false;
}


bool IGameController::OnEntity(int Index, vec2 Pos)
{
	int Type = -1;
	int SubType = 0;

	if(Index == ENTITY_SPAWN_RED)
		m_aaSpawnPoints[0].add(Pos);
	else if(Index == ENTITY_SPAWN_BLUE)
		m_aaSpawnPoints[1].add(Pos);

	return false;
}

void IGameController::EndRound()
{
	if(m_Warmup) // game can't end when we are running warmup
		return;

	GameServer()->m_World.m_Paused = true;
	m_GameOverTick = Server()->Tick();
	m_SuddenDeath = 0;
}

void IGameController::ResetGame()
{
	GameServer()->m_World.m_ResetRequested = true;
}

const char *IGameController::GetTeamName(int Team)
{
	if(IsTeamplay())
	{
		if(Team == TEAM_RED)
			return _("red team");
		else if(Team == TEAM_BLUE)
			return _("blue team");
	}else if(Team != TEAM_SPECTATORS)
		return _("game");

	return _("spectators");
}

static bool IsSeparator(char c) { return c == ';' || c == ' ' || c == ',' || c == '\t'; }

void IGameController::StartRound()
{
	ResetGame();

	m_RoundStartTick = Server()->Tick();
	m_SuddenDeath = 0;
	m_GameOverTick = -1;
	GameServer()->m_World.m_Paused = false;
	m_aTeamscore[TEAM_RED] = 0;
	m_aTeamscore[TEAM_BLUE] = 0;
	m_ForceBalanced = false;
	Server()->DemoRecorder_HandleAutoStart();
	char aBuf[256];
	str_format(aBuf, sizeof(aBuf), "start round type='%s' teamplay='%d'", m_pGameType, m_GameFlags&GAMEFLAG_TEAMS);
	GameServer()->Console()->Print(IConsole::OUTPUT_LEVEL_DEBUG, "game", aBuf);

	for (auto pPlayer : m_pGameServer->m_apPlayers) 
	{
		if (!pPlayer) 
			continue;

		if(pPlayer->GetTeam() == TEAM_SPECTATORS)
			continue;

		pPlayer->CureToDefault();
	}
}

